export function getChainConfig(chainId: number): NetworkConfig | undefined {
  return CONTRACTS[chainId]
}

export function getChainName(chainId: number): string {
  return CONTRACTS[chainId]?.name ?? `Chain ${chainId}`
}

export function isL2Chain(chainId: number): boolean {
  return (L2_CHAINS as readonly number[]).includes(chainId)
}
